#demonstrate set operations in python
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}
print("Set A:", A)
print("Set B:", B)

# Union
union_set = A | B
print("Union of A and B:", union_set)
# Intersection
intersection_set = A & B
print("Intersection of A and B:", intersection_set)
# Difference
difference_set = A - B
print("Difference of A and B (A - B):", difference_set)



